#include <common.hpp>
#include <chrono>

int64_t get_ts()
{
    auto now = std::chrono::steady_clock::now();
    return static_cast<int64_t>(std::chrono::time_point_cast<std::chrono::microseconds>(now).time_since_epoch().count());
}

